package Section7.RoomOfHouse;

public class Ceiling {
    private int height;
    private int paintedColor;

    public int getHeight() {
        return height;
    }

    public int getPaintedColor() {
        return paintedColor;
    }

    public Ceiling(int height, int paintedColour) {
        this.height = height;
        this.paintedColor = paintedColour;
    }
}
